<h3>"What treatments does my favorite gene respond to?" Instructions</h3>


<p>
This query allows you to search for gene(s) on the EDGE chip that show a response greater than or equal to the <strong><font color="blue">Minimum Induction</font></strong>
value specified or less than or equal to the <strong><font color="blue">Minimum Repression</font></strong> value specified.  You can search by <strong><font color="red">Gene Name</font></strong>,
<strong><font color="red">REFSEQ</font></strong>, <strong><font color="red">GO ID</font></strong>, and/or
<strong><font color="red">GO Term</font></strong>. </p>
 <p>You are not limited to searching on one field only; you can enter queryable items in all fields if you wish.  To enter multiple values in the fields, delimit the values by commas.
Partial values will bring back anything that matches the partial value entered.
