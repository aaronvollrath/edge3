<h4>"What tissues respond to my treatment?" Instructions</h4>


<p>
This query allows you to search for tissues that respond to a specific chemical or chemicals based on a fold-change response greater than or equal to the <strong><font color="blue">Minimum Induction</font></strong>
value specified or less than or equal to the <strong><font color="blue">Minimum Repression</font></strong> value specified. </p>

<p>The results will consist of a list of tissues treated by the selected treatment(s) and the number of
genes that met the criteria set by the fold-change threshold values specified.</p>
