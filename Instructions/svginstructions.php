<h3>SVG Instructions</h3>
<p><strong><font color="red">Note:</font></strong>  These instructions are applicable to the IE Adobe SVG Viewer plugin on a Windows platform.</p>

<p><strong>Panning:</strong> <br>Hold the ALT key and click-and-drag with the mouse to pan an SVG image. When the scroll lock is enabled and Adobe SVG Viewer has the focus, the arrow keys may be used to pan the image.
</p>
<p><strong>Zooming:</strong> <br>Hold the CTRL key and click to zoom in at the mouse pointer location. Hold the CTRL key and click-and-drag to select a region to zoom into. Hold the CTRL and SHIFT keys and click to zoom out. You can also use the zoom commands in the right-click context menu.
</p>
